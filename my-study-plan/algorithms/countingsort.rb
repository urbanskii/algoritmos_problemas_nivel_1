



def counting_sort(arr)
    i = 0
    arr_counting = 0

    while counting = false
        arr.each do i
        p i         
        if arr[i] > arr[i+1]
            arr_counting >= arr[i]
                arr_counting = arr[i]
        end
        i += 1
        if arr[i+1] == nil
            counting = true
        end
    end

end


